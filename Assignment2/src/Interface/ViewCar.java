     private boolean brandNamePatternCorrect(){
        Pattern p =Pattern.compile("^[a-zA-Z]+$");
        Matcher m = p.matcher(txtBrandName.getText());
        boolean b = m.matches();
        return b;
    }
    
    
    
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        try {
           cars.setExpirationDate(new SimpleDateFormat("dd/MM/yyyy").parse(txtExpirationDate.getText()));
       } catch (ParseException ex) {
           System.out.println("Date error");
       }
        
        
        
        if(checkBox.isSelected()){
            txtStatus.setText("Y");
            
        }
        else{
            txtStatus.setText("N");
        }
        
        String status = txtStatus.getText();
        
         String specialCharacters=" !#$%&'()*+,-./:;<=>?@[]^_`{|}~";
          
          String str2[]=txtModelNumber.getText().split("");

          
          
          
        
        try{
         int yearOfManufacturing = Integer.parseInt(txtYearOfManufacturing.getText());
         int numberOfSeats = Integer.parseInt(txtNumberOfSeats.getText());
         
         
         
         if(txtModelNumber.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Enter Model Number");
        }
        else{
            if(txtBrandName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Enter Brand Name");
        }
            else{
                if(txtSerialNumber.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Enter Serial Number");
        }
                else{
                    if (brandNamePatternCorrect()==false){
                        JOptionPane.showMessageDialog(null, "Brand Name should only contain Char");
                    }
                    else{
                        
                    
                    if(String.valueOf(txtExpirationDate.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Expiration Date");
            
        }
                    
                    else{
                        
                        
                        
                        
                        cars.setModelNumber(txtModelNumber.getText());
                        cars.setBrandName(txtBrandName.getText());
                        cars.setNumberOfSeats(Integer.parseInt(txtNumberOfSeats.getText()));
                        cars.setSerialNumber(txtSerialNumber.getText());
                        cars.setYearOfManufacturing(Integer.parseInt(txtYearOfManufacturing.getText()));
                        cars.setStatus(txtStatus.getText());
                        
                        cars.setLocation(txtLocation.getText());
                        cars.setCity(txtCity.getText());
                        cars.setCreatedOn(new Date());
                        
                        
                        btnSave.setEnabled(false);
                        btnUpdate.setEnabled(true);
        
                        JOptionPane.showMessageDialog(null,"Cars Updated Successfully");
        
        
                    }
                    }
                }
            }
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Year of Manufacturing and Number of seats should only be int");
           
        }
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

