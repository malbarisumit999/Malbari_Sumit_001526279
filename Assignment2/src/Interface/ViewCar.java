    public void populateCarDetails(){
        txtModelNumber.setText(cars.getModelNumber());
        txtBrandName.setText(cars.getBrandName());
        txtNumberOfSeats.setText(String.valueOf(cars.getNumberOfSeats()));
        txtSerialNumber.setText(cars.getSerialNumber());
        txtYearOfManufacturing.setText(String.valueOf(cars.getYearOfManufacturing()));
        txtStatus.setText(cars.getStatus());
        txtExpirationDate.setText(toformat(cars.getExpirationDate()));
        txtLocation.setText(cars.getLocation());
        txtCity.setText(cars.getCity());
        txtDate.setText(toformat(cars.getCreatedOn()));
    }
    
    public String toformat(Date date){
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        String b = formatter.format(date);
        return b;
    }
